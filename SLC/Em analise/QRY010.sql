SD3.D3_EMISSAO, SD3.D3_DOC, SD3.D3_CF, SD3.D3_CUSTO1, SD3.D3_QUANT  
FROM  SD3010 SD3 with(nolock)  
JOIN  SB1010 SB1 with(nolock)   ON B1_FILIAL = '  '  AND D3_COD    = B1_COD  AND SB1.D_E_L_E_T_ = ' ' 
JOIN  SB5010 SB5 with(nolock)   ON B5_FILIAL = '  '  AND B5_COD = B1_COD  AND SB5.D_E_L_E_T_ = ' '  
JOIN  SBM010 SBM with(nolock)   ON BM_FILIAL = '  '  AND SB1.B1_GRUPO = SBM.BM_GRUPO  AND SBM.D_E_L_E_T_ = ' '  
WHERE D3_FILIAL = '04'  AND NOT EXISTS ( SELECT NULL   FROM  VB8010 VB8 with(nolock)   WHERE VB8_FILIAL = '04'  
AND VB8_PRODUT = SD3.D3_COD  AND (VB8_ANO + VB8_MES + VB8_DIA) >= '20250821'  AND VB8.D_E_L_E_T_ = ' '  
AND VB8_FLGENV = ' '  )  AND SD3.D3_EMISSAO >= '20250821' AND SD3.D_E_L_E_T_ = ' ' 
AND ( B1_GRUPO IN  ('200','205','210','215','220','211','212','213','253','254','256','257','258','216','291','292','293','296','297','298','295','264')  
OR B5_ISDSHIP='1' ) 
